package homework.day1.pretask;

public class IncrementDecrementExample {

    public static void main(String[] args) {

        int variable = 7;
        ++variable;

        System.out.println(variable); // можно прям сразу вписать ++variable без строчки вверху, оно бы сразу реализовалось тут

        --variable;

        System.out.println(variable);
    }
}
