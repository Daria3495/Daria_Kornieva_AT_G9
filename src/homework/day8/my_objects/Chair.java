package homework.day8.my_objects;

import java.util.Objects;

public class Chair {

    private int width;

    private int height;

    public Chair(int width, int height) {
        this.width = width;
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    @Override
    public String toString() {
        return "Chair{" +
                "width=" + width +
                ", height=" + height +
                '}';
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        Chair chair = (Chair) o;
        return width == chair.width && height == chair.height;
    }

    @Override
    public int hashCode() {
        return Objects.hash(width, height);
    }
}
